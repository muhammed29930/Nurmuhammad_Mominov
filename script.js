// --- LANGUAGE SWITCHING FUNCTION ---
function setLanguage(lang) {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const keys = element.getAttribute('data-i18n').split('.');
        let translation = translations[lang];
        for (const key of keys) {
            translation = translation[key];
        }
        if (translation) {
            // Use innerHTML for elements that might contain HTML tags
            if (element.innerHTML.includes('<strong>')) {
                element.innerHTML = translation;
            } else {
                element.textContent = translation;
            }
        }
    });
    // Save language preference to localStorage
    localStorage.setItem('preferredLanguage', lang);
}

// --- DOCUMENT READY ---
document.addEventListener('DOMContentLoaded', () => {
    // --- Language Initialization ---
    const languageSelector = document.getElementById('languageSelector');
    const savedLanguage = localStorage.getItem('preferredLanguage') || 'en';
    languageSelector.value = savedLanguage;
    setLanguage(savedLanguage);

    // Language Selector Event Listener
    languageSelector.addEventListener('change', (e) => {
        setLanguage(e.target.value);
    });

    // --- Navigation ---
    const desktopNavLinks = document.querySelectorAll('.desktop-nav-list a');
    const mobileNavLinks = document.querySelectorAll('.mobile-nav-items a');
    const allNavLinks = [...desktopNavLinks, ...mobileNavLinks, ...document.querySelectorAll('.btn[data-page]')];
    const pages = document.querySelectorAll('.page');

    allNavLinks.forEach(link => {
        link.addEventListener('click', (e) => {
            e.preventDefault();
            const targetPage = link.getAttribute('data-page');
            
            // Update active states
            desktopNavLinks.forEach(a => a.classList.remove('active'));
            mobileNavLinks.forEach(a => a.classList.remove('active'));
            
            // Set the active class on the clicked link
            link.classList.add('active');
            
            // Ensure both desktop and mobile links are updated when one is clicked
            if (link.closest('.desktop-nav-list')) {
                // Clicked desktop link: update matching mobile link
                const matchingMobile = Array.from(mobileNavLinks).find(a => a.getAttribute('data-page') === targetPage);
                if (matchingMobile) matchingMobile.classList.add('active');
            } else if (link.closest('.mobile-nav-items')) {
                // Clicked mobile link: update matching desktop link
                const matchingDesktop = Array.from(desktopNavLinks).find(a => a.getAttribute('data-page') === targetPage);
                if (matchingDesktop) matchingDesktop.classList.add('active');
            } 
            
            // Show target page
            pages.forEach(page => {
                if (page.id === targetPage) {
                    page.classList.remove('hidden');
                } else {
                    page.classList.add('hidden');
                }
            });

            window.scrollTo({ top: 0, behavior: 'smooth' });
        });
    });

    // --- Theme Toggle (Your existing code) ---
    const themeSwitch = document.getElementById('themeSwitch');
    const body = document.body;

    const currentTheme = localStorage.getItem('theme');
    if (currentTheme === 'light') {
        body.classList.add('light-mode');
    }

    themeSwitch.addEventListener('click', () => {
        body.classList.toggle('light-mode');
        
        if (body.classList.contains('light-mode')) {
            localStorage.setItem('theme', 'light');
        } else {
            localStorage.setItem('theme', 'dark');
        }
    });
});